    public int numEnclaves(int[][] grid) {
        int rows = grid.length;
        int columns = grid[0].length;
        boolean[][] flags = new boolean[rows][columns];


        int ans = 0;

        for (int i = 0; i < rows; i++) {
            dfs(grid,i,0,flags);
            dfs(grid,i,columns-1,flags);
        }

        for (int i = 0; i < columns; i++) {
            dfs(grid,0,i,flags);
            dfs(grid,rows-1,i,flags);
        }
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < columns; j++) {
                if(grid[i][j]==1){
                    ans++;
                }
            }
        }
        return ans;

    }


    private void dfs(int[][] grid, int x, int y, boolean[][] flags) {

        if (grid[x][y] == 1) {
            flags[x][y] = true;
            grid[x][y] = -1;
            for (int i = 0; i < 4; i++) {
                int tempX = x + dx[i];
                int tempY = y + dy[i];
                if (tempX >= 0 && tempY >= 0 && tempX < grid.length && tempY < grid[0].length && !flags[tempX][tempY]) {
                    dfs(grid, tempX, tempY, flags);
                }
            }
            flags[x][y]=false;
        }
    }

